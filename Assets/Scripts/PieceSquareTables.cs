    public static int GetValue(Piece piece){
        int[,] table;
        switch (piece){
            case Pawn p:
                table = _pawnTable;
                break;
            case Knight k:
                table = _knightTable;
                break;
            case Bishop b:
                table = _bishopTable;
                break;
            case Rook r:
                table = _rookTable;
                break;
            case Queen q:
                table = _queenTable;
                break;
            case King k:
                table = _kingTable;
                break;
            default:
                throw new NotImplementedException();
        }

        var y = piece.Coordinates % 8;
        var x = (piece.Coordinates - y) / 8;
        
        return piece.Team == Team.White ? table[x, y] : table[7-x, y];
    }
}